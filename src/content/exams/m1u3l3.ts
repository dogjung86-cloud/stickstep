const pointY = (spec: MExamChangeGraphSpec, x: number): number => {
  for (const series of spec.series) {
    const point = series.points.find(([px]) => px === x);
    if (point) return point[1];
  }
  throw new Error(`그래프에 x=${x}인 점이 없습니다.`);
};

